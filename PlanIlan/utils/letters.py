def print_letters(text, scale=1, e_space=2):
    print('\n\n')
    big_letters = []
    for i in text:
        big_letters.append(letters.get(i.lower(), letters[' ']))
    output = [''] * (5 * scale)
    for i in range(5):
        for ind, j in enumerate(big_letters):
            temp = ' '
            try:
                temp = j[i]
            except:
                pass
            line = ''
            for z in temp:
                line += z * scale
            line += ' ' * (3 * scale + e_space - len(line))
            line = line.replace('#', text[ind].upper())
            output[i * scale] += line
        for bold in range(scale - 1):
            output[i * scale + bold + 1] = output[i * scale]

    return '\n'.join(output) + '\n\n'
